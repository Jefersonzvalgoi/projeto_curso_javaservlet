package model;

public class Profissional {
	private int id;
	private String contatoReferencia;
	private String descricaoPessoal;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getContatoReferencia() {
		return contatoReferencia;
	}
	public void setContatoReferencia(String contatoReferencia) {
		this.contatoReferencia = contatoReferencia;
	}
	public String getDescricaoPessoal() {
		return descricaoPessoal;
	}
	public void setDescricaoPessoal(String descricaoPessoal) {
		this.descricaoPessoal = descricaoPessoal;
	}
}
